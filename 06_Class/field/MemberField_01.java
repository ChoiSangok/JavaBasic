package java06_class.field;

public class MemberField_01 { //기능클래스

	//멤버필드
	//접근제한자 private
	private int num=888;
	private String name="Alice";
	//Generate Getter and Setters 단축키 : alt+ shift+ s

	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	


	
	
	
}
