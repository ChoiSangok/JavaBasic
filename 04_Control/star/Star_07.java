package java04_control.star;

public class Star_07 {
	public static void main(String[] args) {
		
		int i,j;
		
		for (i=5;i>=1;i--) { //quiz 7    
     
			for(j=i;j<i+5;j++) {        
				System.out.print(j);   
			}                      
			System.out.println();
		}
		//56789
		//45678
		//34567
		//23456
		//12345
	}


}
