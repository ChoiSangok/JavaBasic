package java04_control.star;

public class Star_10 {


	public static void main(String[] args) {
		
		int i,j;
		
	      
	     for(i=0; i<=5 ; i++) { //quiz 
	        for(j=0;j<5;j++) { 
	           if(i<=j) {
	              System.out.print("*");     
	           }                         	 
	           else {                        
	              System.out.print(" ");     
	           }                                      
	     }
	        System.out.println("");
	       
	   }
	     //*****
	     // ****
	     //  ***
	     //   **
	     //    *
	}

}
