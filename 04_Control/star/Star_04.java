package java04_control.star;

public class Star_04 {

	public static void main(String[] args) {
		
		
		int i,j;
		
		for(i=1;i<6;i++) {// 1, 1-5, 5
     
			for(j=0;j<5;j++) {     //0 0-4 5      
				System.out.print(i);     
			}                        
			System.out.println(); //띄어쓰기

		}
		//11111
		//22222
		//33333
		//44444
		//55555

	}

}
