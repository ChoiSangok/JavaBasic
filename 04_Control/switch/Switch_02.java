package java04_control.switchCase;

public class Switch_02 {
	public static void main(String[] args) {
		
		
		//switch는 비교대상으로 연산식을 넣을 수 있다.
		
		int num=20;
		
		switch(num-5) {
		
		case 10:
			System.out.println("1번 영역");
			break;
		case 15:
			System.out.println("1번 영역");
			break;
		case 20:
			System.out.println("1번 영역");
			break;
			
		
		}
	}
}
